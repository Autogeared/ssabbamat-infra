export function buildInsightsPrompt(layoutSummary: string, metricsSummary: string): string {
  return `당신은 포장/배달 전문 매장의 동선 최적화 컨설턴트입니다.

## 현재 레이아웃 정보
${layoutSummary}

## 현재 동선 메트릭
${metricsSummary}

## 분석 요청
아래 항목을 한국어로 분석해주세요. 구체적인 숫자와 방향(예: "2m 남쪽으로")을 포함해주세요.

1. **동선 효율성 평가** — 현재 동선의 효율성을 100점 만점으로 평가
2. **병목 분석** — 가장 비효율적인 구간과 원인
3. **레이아웃 개선 제안** — 장비 재배치 제안 (최소 2개, 구체적 위치)
4. **안전/위생 체크** — 동선 교차로 인한 안전 위험, 냉장고-가스레인지 거리 등
5. **예상 개선 효과** — 제안 적용 시 예상 시간/거리 절감률

마크다운 형식으로 작성해주세요.`;
}
